package operators.arithmetic;

public class Demo4 {
    public static void main(String[] args) {
        // / , %
        System.out.println(1 / 10);//Quotient = 0
        System.out.println(1 % 10); //Remainder = 1

        System.out.println(14 % 2);//R=0
        System.out.println(14 / 2);//Q = 7

        System.out.println(10 + 20 + 'A');//95
        System.out.println(10 + 20 * 3 - 50);//20

        System.out.println(10 + 20 + "A" + 10 + 20);//30A1020
    }
}
