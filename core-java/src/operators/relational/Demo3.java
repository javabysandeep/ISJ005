package operators.relational;

public class Demo3 {
    public static void main(String[] args) {
        System.out.println(10 % 2 == 0);//true
        System.out.println(10 % 2 != 0);//false

        System.out.println(2020 % 4 == 0);//true
        System.out.println(2000 % 400 == 0);//true
        System.out.println(2023 % 100 != 0);//true
    }
}
