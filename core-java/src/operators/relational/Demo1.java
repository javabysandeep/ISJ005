package operators.relational;

public class Demo1 {
    public static void main(String[] args) {
        //>, <, >=, <=, ==, !=
        System.out.println(10 > 20); //false
        System.out.println(10 < 20); //true
        System.out.println(10 >= 20); //false
        System.out.println(10 <= 20);//true
        System.out.println(10 == 20);//false
        System.out.println(10 != 20);//true
    }
}
